package com.garganttua.core.injection;

/**
 * Holds the predefined provider scope names used by the framework's built-in
 * bean and property providers.
 *
 * @since 2.0.0-ALPHA01
 */
public class Predefined {

    /**
     * Predefined bean provider scope names.
     */
    public enum BeanProviders{
        /** The default framework-managed bean provider scope. */
        garganttua
    }

    /**
     * Predefined property provider scope names.
     */
    public enum PropertyProviders{
        /** The default framework-managed property provider scope. */
        garganttua
    }

}
